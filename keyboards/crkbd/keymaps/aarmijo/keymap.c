void set_keylog(uint16_t keycode, keyrecord_t *record) {
  
  kc = keycode;
  row = numbers[record->event.key.row];
  col = numbers[record->event.key.col];

  if ((keycode >= QK_MOD_TAP && keycode <= QK_MOD_TAP_MAX) || (keycode >= QK_LAYER_TAP && keycode <= QK_LAYER_TAP_MAX)) {
    keycode = keycode & 0xFF;
  }

  if (keycode < 60) {
    keyName = letters[keycode];
  }

}

void oled_render_keylog(int x, int y) {

    oled_set_cursor(x, y);
    oled_write_P(PSTR("KC"), false);
    oled_write(get_u8_str(kc, '0'), false);

    oled_set_cursor(x, y + 1);
    oled_write_P(PSTR(" "), false);
    oled_write_char(row, false);
    oled_write_P(PSTR("x"), false);
    oled_write_char(col, false);
    oled_write_P(PSTR("  "), false);

    oled_set_cursor(x, y + 2);
    oled_write_P(PSTR("  "), false);
    oled_write_char(keyName, false);
    oled_write_P(PSTR("  "), false);

}

