bool process_record_user(uint16_t keycode, keyrecord_t *record) {

  if (record->event.pressed) {
    set_keylog(keycode, record);
    isLunaAwake = true;
  }

  switch (keycode) {
    case KC_LCTL:
    case KC_RCTL:
    case KC_RALT: 
    case KC_LALT: 
    case KC_LGUI:
    case KC_RGUI:
        if (record->event.pressed) {
            isLunaSneaking = true;
        } else {
            isLunaSneaking = false;
        }
        break;
    case KC_ESC:
    case QK_GESC:
    case LT(1, KC_TAB):
    case KC_SPC:
    case LCTL_T(KC_SPC):
    case RGUI_T(KC_SPC):
    case RSFT_T(KC_ENT):
        if (record->event.pressed) {
            isLunaJumping  = true;
            hasLunaJumped = false;
        } else {
            isLunaJumping = false;
        }
        break;
    case RUT:
      if (record->event.pressed) {
        SEND_STRING("13658100-7");
      }
      break;
    case EMAIL:
      if (record->event.pressed) {
        SEND_STRING("arielarmijo@yahoo.es");
      }
      break;
    case ARROW_FN:
      if (record->event.pressed) {
        SEND_STRING("() => {}");
      }
      break;
  }

  return true;

}

